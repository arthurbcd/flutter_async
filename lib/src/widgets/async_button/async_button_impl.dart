part of 'async_button.dart';

/// Async version of [ElevatedButton].
class AsyncElevatedButton extends AsyncButton<ElevatedButton> {
  /// Creates an [AsyncButton] of [ElevatedButton].
  const AsyncElevatedButton({
    super.key,
    super.config,
    super.onPressed,
    super.onLongPress,
    super.onHover,
    super.onFocusChange,
    super.style,
    super.focusNode,
    super.autofocus,
    super.clipBehavior,
    super.statesController,
    required super.child,
  });
}

